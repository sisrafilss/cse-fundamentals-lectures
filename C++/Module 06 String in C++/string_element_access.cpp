#include<bits/stdc++.h>
using namespace std;

int main()
{
    string st = "abcdef";

    // at() function return the n'th index value
    cout << st.at(4) << endl; // will return "e"

    cout << st.front() << endl; // front() return the first value from left
    cout << st.back() << endl; // back() return the last value.

    return 0;
}
